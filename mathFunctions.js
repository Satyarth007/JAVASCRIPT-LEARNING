// PI VALUE
console.log(Math.PI);  // 3.141592653589793

// EULER VALUE
console.log(Math.E);    //2.718281828459045

// ROUND FUNCTION
console.log(Math.round(5.2));   //5
console.log(Math.round(5.6));   //6

// CEIL AND FLOOR FUNCTION
console.log(Math.ceil(5.2));    //6
console.log(Math.floor(5.2));   //5

//TRUNCATE FUNCTION
console.log(Math.trunc(4.9));   //4

// MAX-MIN FUNCTION
console.log(Math.max(5,8,19,2));    //19
console.log(Math.min(5,8,19,2));    //2

//POWER FUNCTION
console.log(Math.pow(2,3));         //8

//SQUARE ROOT AND CUBE ROOT FUNCTION
console.log(Math.sqrt(4));      //2
console.log(Math.cbrt(8));      //2

// SIGN FUNCTION
console.log(Math.sign(-8));     //-1
console.log(Math.sign(8));      //1

